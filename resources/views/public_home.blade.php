            $.get('{{ url('autocomplete_brand') }}?category_id=' + category, function(data) {
                console.log(data);
                $('#dev-brand').empty();
                $.each(data, function(index,subCatObj){
                     $('<option/>').attr('value',subCatObj.id).text(subCatObj.name).appendTo('#dev-brand');
                });
            });
            /*"<option value='"+subCatObj.id+"'>"+subCatObj.name+"</option>"*/
        });
        $('#dev-model').on('focus', function(){
            var brand = $('#dev-brand').val();
            $.get('{{ url('autocomplete_model') }}?brand_id=' + brand, function(data) {
                console.log(data);
                $('#dev-model').empty();
                $.each(data, function(index,deviceObj){
                     $('<option/>').attr('value',deviceObj.id).text(deviceObj.model).appendTo('#dev-model');
                });
            });
        });
